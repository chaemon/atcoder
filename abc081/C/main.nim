proc solve(N:int, K:int, A:seq[int]) =
  var tb = initTable[int,int]()
  for i in 0..<N:
    tb[A[i]] += 1
  if tb.len <= K:
    echo 0
  else:
    var v = newSeq[(int,int)]()
    for key,val in tb:
      v.add((val, key))
    v.sort()
    v.reverse()
    var ans = 0
    for i in K..<v.len: ans += v[i][0]
    echo ans
  return

#{{{ main function
proc main() =
  var N = 0
  N = nextInt()
  var K = 0
  K = nextInt()
  var A = newSeqWith(N, 0)
  for i in 0..<N:
    A[i] = nextInt()
  solve(N, K, A);
  return

main()
