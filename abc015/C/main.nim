var N:int
var K:int
var T:seq[seq[int]]

proc solve() =
  tb := newSeqWith(128, false)
  tb[0] = true
  for i in 0..<N:
    tb2 := newSeqWith(128, false)
    for j in 0..<K:
      for xi,x in tb:
        if not x: continue
        yi := (xi xor T[i][j])
        if yi == 0: echo "Found";return
        tb2[yi] = true
    swap(tb, tb2)
  echo "Nothing"
  return

#{{{ input part
block:
  N = nextInt()
  K = nextInt()
  T = newSeqWith(N, newSeqWith(K, nextInt()))
  solve()
