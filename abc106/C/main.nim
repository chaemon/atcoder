proc solve(S:string, K:int) =
  var
    i = 0
    d = -1
  while i < S.len:
    if S[i] != '1':
      d = S[i].ord - '0'.ord
      break
    i += 1
  if K < i: echo 1
  else: echo d
  return

#{{{ main function
proc main() =
  var S = ""
  S = nextString()
  var K = 0
  K = nextInt() - 1
  solve(S, K);
  return

main()
