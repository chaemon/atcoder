const YES = "YES"
const NO = "NO"
var N:int
var a:int
var b:int
var K:int
var P:seq[int]

proc solve() =
  var s = initSet[int]()
  s.incl(a)
  for i in 0..<P.len:
    if P[i] in s: echo NO;return
    s.incl(P[i])
  if b in s: echo NO;return
  echo YES
  return

#{{{ input part
block:
  N = nextInt()
  a = nextInt() - 1
  b = nextInt() - 1
  K = nextInt()
  P = newSeqWith(K, nextInt() - 1)
  solve()
#}}}
