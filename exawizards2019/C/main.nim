#{{{ findFirst(f, a..b), findLast(f, a..b)
proc findFirst(f:(int)->bool, s:Slice[int]):int =
  var (l, r) = (s.a - 1, s.b)
  if not f(r): return s.b + 1
  while r - l > 1:
    let m = (l + r) div 2
    if f(m): r = m
    else: l = m
  return r
proc findLast(f:(int)->bool, s:Slice[int]):int =
  var (l, r) = (s.a, s.b + 1)
  if not f(l): return s.a - 1
  while r - l > 1:
    let m = (l + r) div 2
    if f(m): l = m
    else: r = m
  return l
#}}}

proc main() =
  proc left(i:int):bool =
    var i = i
    for j in 0..<Q:
      if t[j][0] != s[i]: continue
      if d[j][0] == 'L':i.dec
      else: i.inc
      if i < 0: return false
      elif i >= N: return true
    return true
  proc right(i:int):bool =
    var i = i
    for j in 0..<Q:
      if t[j][0] != s[i]: continue
      if d[j][0] == 'L':i.dec
      else: i.inc
      if i >= N: return false
      elif i < 0: return true
    return true
  let
    l = findFirst(left, 0..<N)
    r = findLast(right, 0..<N)
  if l > r: print 0
  else: print r - l + 1
  return

main()
