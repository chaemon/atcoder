var N:int
var A:int
var x:seq[int]

proc solve() =
  let X = x.sum
  dp := newSeqWith(N+1, newSeqWith(X+1, 0))
  ans := 0
  dp[0][0] = 1
  for i in 0..<N:
    dp2 := dp
    for k in 0..i:
      for s in 0..<dp[0].len:
        if dp[k][s] == 0: continue
        let s2 = s + x[i]
        dp2[k+1][s2] += dp[k][s]
    swap(dp, dp2)
  for k in 1..N:
    if k * A >= dp[k].len: continue
    ans += dp[k][k*A]
  echo ans
  return

#{{{ input part
block:
  N = nextInt()
  A = nextInt()
  x = newSeqWith(N, nextInt())
  solve()
#}}}
