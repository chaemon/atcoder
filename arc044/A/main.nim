# isPrime {{{
proc isPrime(x:int):bool =
  if x == 1: return false
  var i = 2
  while i * i <= x:
    if x mod i == 0: return false
    i += 1
  return true
#}}}

proc solve() =
  if isPrime(N): echo "Prime";return
  if N == 1: echo "Not Prime";return
  var N = N
  v := newSeq[int]()
  while N > 0:
    v.add(N mod 10)
    N = N div 10
  if v[0] mod 2 != 0 and v[0] mod 5 != 0 and v.sum mod 3 != 0: echo "Prime";return
  echo "Not Prime"
  return

