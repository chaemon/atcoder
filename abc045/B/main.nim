var S_A:string
var S_B:string
var S_C:string

proc solve() =
  var (i,j,k) = (0,0,0)
  cur := 'a'
  while true:
    if cur == 'a':
      if S_A.len == i:
        echo 'A'
        break
      cur = S_A[i]
      i += 1
    elif cur == 'b':
      if S_B.len == j:
        echo 'B'
        break
      cur = S_B[j]
      j += 1
    elif cur == 'c':
      if S_C.len == k:
        echo 'C'
        break
      cur = S_C[k]
      k += 1
    else:
      assert(false)
  return

#{{{ input part
block:
  S_A = nextString()
  S_B = nextString()
  S_C = nextString()
  solve()
#}}}
