var N:int
var s:seq[string]

#{{{ input part
proc main()
block:
  N = nextInt()
  s = newSeqWith(N, nextString())
#}}}

main()

proc main() =
  var
    ans = 0
    a = 0
    b = 0
    ba = 0
  for s in s:
    for i in 0..<s.len - 1:
      if s[i] == 'A' and s[i+1] == 'B': ans += 1
    if s[0] == 'B' and s[^1] == 'A': ba += 1
    elif s[^1] == 'A': a += 1
    elif s[0] == 'B': b += 1
  if ba > 0:
    ans += ba - 1
    if a > 0: ans += 1; a -= 1
    if b > 0: ans += 1; b -= 1
  ans += min(a, b)
  echo ans
  return
